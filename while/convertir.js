var x = 5;
var convertirCast;
function convertirCast(x) {
    if (x == 1){
        return "uno";
    }
    else {
        if (x == 2) {
            return "dos";
        }
    else {
        if (x == 3){
            return "tres";
        }
    else {
        if (x == 4) {
            return "cuatro";
        }
    else {
        if (x == 5){
            return "cinco";
        }
    else {
        return "valor incorrecto";
    }
    }
    }
    }
    }
}
console.log(convertirCast(x));
